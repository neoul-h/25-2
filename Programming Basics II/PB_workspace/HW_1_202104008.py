# -------------------------------------------------
# [프로그램 설명]
# 이 프로그램은 사용자에게서 소문자 'a', 't', 'g', 'c' 로만 이루어진
# DNA 염기서열 문자열을 입력받아서, 각 글자가 몇 번 나오는지 개수를 센 후 출력한다.
#
# [변수 설명]
# dna_input : 사용자가 입력한 전체 문자열
# cnt_a, cnt_t, cnt_g, cnt_c : 각각 'a','t','g','c'가 등장한 횟수를 저장하는 정수 변수
#
# [로직 설명]
# 1) 학번/이름을 먼저 출력한다.
# 2) DNA 염기서열(문자열)을 input()으로 입력받는다.
# 3) for 반복문으로 문자열을 한 글자씩 검사하면서 if/elif로 어떤 글자인지 확인하고
#    해당 글자의 개수를 1씩 증가시킨다.
# 4) 마지막에 a, t, g, c 의 개수를 순서대로 print 한다.
# -------------------------------------------------

print("학번: 202104008, 이름: 김진우")

# 1) 사용자에게 DNA 염기서열 입력받기
dna_input = input("DNA 염기서열을 입력하세요 (a/t/g/c만 사용): ")

# 2) 개수 세는 변수들(처음은 전부 0)
cnt_a = 0
cnt_t = 0
cnt_g = 0
cnt_c = 0

# 3) 문자열의 각 글자를 하나씩 확인
for ch in dna_input:
    if ch == 'a':
        cnt_a = cnt_a + 1
    elif ch == 't':
        cnt_t = cnt_t + 1
    elif ch == 'g':
        cnt_g = cnt_g + 1
    elif ch == 'c':
        cnt_c = cnt_c + 1
    # 다른 글자가 들어올 가능성은 없다고 가정

# 4) 결과 출력
print("a:", cnt_a)
print("t:", cnt_t)
print("g:", cnt_g)
print("c:", cnt_c)