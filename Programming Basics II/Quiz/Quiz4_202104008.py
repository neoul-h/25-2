yes_cnt = 0   # 찬성 개수
no_cnt = 0    # 반대 개수

while True:
    vote = input("투표 의견을 입력하세요 (Y/N, 종료는 Q): ")

    # 종료 조건: Q 또는 q가 들어오면 반복 끝냄
    if vote == 'Q' or vote == 'q':
        break

    # 찬성 처리: 대소문자 구분 없이 Y면 찬성 1 증가
    if vote == 'Y' or vote == 'y':
        yes_cnt = yes_cnt + 1

    # 반대 처리: 대소문자 구분 없이 N이면 반대 1 증가
    elif vote == 'N' or vote == 'n':
        no_cnt = no_cnt + 1

    # 그 외 입력은 무효 처리 알림만 (점수/개수는 변화 없음)
    else:
        print("잘못된 입력입니다. 무효 처리합니다.")
        # 여기서는 안내 출력만 하고 다음 반복으로 넘어갑니다.

# 최종 결과 출력
print("---------------------------")
print("투표 종료")
print("찬성:", yes_cnt)
print("반대:", no_cnt)
