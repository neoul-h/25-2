// slice → (복사본) splice → reduce 순서로 평가된다.
// 주의: slice는 "얕은 복사"를 반환하고 원본 배열은 변경하지 않는다.
//       splice는 "해당 배열에서 제거된 요소들"을 반환한다(여기선 slice 결과 배열에 적용).

let fruits = ['귤', '레몬', '사과', '수박', '바나나', '복숭아', '자두', '파인애플'];

const result = fruits
  .slice(2, 5)                 // 인덱스 2~4 복사 → ['사과','수박','바나나']
  .splice(1, 3)                // 복사본에서 인덱스 1부터 최대 3개 제거→ ['수박','바나나'] 반환
  .reduce((acc, item) => acc += item.length, 0);  // '수박':2, '바나나':3 → 합 5

console.log(result); // 5

/*
[세부 흐름]
- slice(2,5): ['사과','수박','바나나']
- splice(1,3): 제거된 요소 반환 → ['수박','바나나']
- reduce: '수박'.length(2) + '바나나'.length(3) = 5

[예상 출력]
5
*/
