// 포인트: 함수 선언은 "정의까지" 호이스팅(뒤에 나온 선언이 앞선 선언을 덮어씀)
//        var는 "선언만" 호이스팅(대입은 런타임에 진행)

// (1) 런타임 대입: func ← "1" 버전(하지만 이 줄 전에 함수 선언들이 "정의까지" 호이스팅된다!)
var func = function() { console.log("1"); }

// (2) 함수 선언(정의까지 호이스팅). 나중에 등장하는 선언이 이전 선언을 덮어쓴다.
function func() { console.log("2"); }

// (3) 지금 시점의 func는 (1)에서 "대입"이 수행되어 "1" 버전을 가리킴.
//     (호이스팅된 선언들보다 대입이 우선 적용되었기 때문)
func(); // → "1"

// (4) 다시 런타임 대입: func ← "3" 버전
var func = function() { console.log("3"); }

// (5) 두 번째 함수 선언(정의까지 호이스팅) - 이미 호이스팅 단계에서 처리됨.
//     이 시점엔 추가 동작 없음.
function func() { console.log("4"); }

/*
[실제 흐름 요약]
- 스크립트 시작 시: 함수 선언부들이 "정의까지" 호이스팅되어 가장 마지막 선언 "4"가 유효한 함수 본체가 됨.
- 하지만 런타임에서 (1)의 "대입"이 실행되어 func는 "1" 버전으로 교체됨.
- func() 호출 시점엔 "1" 버전을 가리키므로 "1" 출력.
- 이후 (4)에서 다시 "3"으로 대입.

[예상 출력]
1
*/
